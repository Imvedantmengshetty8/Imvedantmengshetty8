//code for relational operators //
#include <stdio.h>

int main() {
     int main;
     int m,n;
     m= 25;
     n= 75;
     
     //operation for lesser//
    printf("m<n=%d\n",m<n);
    //operation for greater //
    printf("m>n=%d\n",m>n);
    //operation for lesser than //
    printf("m<=n=%d\n",m<=n);
    //operation for greater than //
    printf("m>=n=%d\n",m>=n);
    //operation for equal to//
    printf("m=n=%d\n",m=n);
    //operations for not equal to//
    printf("m!=n=%d\n",m!=n);
    //operation for double equal to//
    printf("m==n=%d\n",m==n);
    
    return 0;
}


